package com.Imooc.flow;

public class IntCompare {
	public static void main(String[] args) {
		//定义两个整型变量初始化
		int x=15, y=5;
		//判断x和y是否相等
		if(x!=y) {
			if(x>y){
				System.out.println(x+">"+y);
			}else {
				System.out.println(x+"<"+y);
			}
		}else {
			System.out.println(x+"和"+y+"相等");
		}
	
	
	
	
	
	
	}

}
