package com.Imooc;

public class FloatDemo {
	public static void main(String[] args) {
		// 定义一个单精度浮点型变量，存放1234.328
		float f=1234.328f;
		System.out.println("f="+f);
		// 定义一个双精度浮点型变量，存放4559.234
		double d=4559.234d;
		System.out.println("d="+d);
		//将整形复制给浮点型
		double d1=123;
		System.out.println("d1= "+d1);
		//变量间的
		double d2=d;
		System.out.println("d2="+d2);
		

	}

}
